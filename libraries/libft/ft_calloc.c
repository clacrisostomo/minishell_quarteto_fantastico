#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*array;

	array = (char *)malloc(size * nmemb);
	if (!array)
		return (NULL);
	return (ft_memset(array, 0, size * nmemb));
}
