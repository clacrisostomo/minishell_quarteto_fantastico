#include "includes/minishell.h"

int ft_isvar(char **cmd)
{
	int	j;

	j = 0;
	while ((cmd[0][j] <= 90 && cmd[0][j] >= 65) || ft_isdigit(cmd[0][j]))
		j++;
	if (cmd[0][j] == '=')
		return(1);
	return(0);
}
